   Odometer.options = {
      duration: 50000000, // Slow animation duration (5 seconds)
   };

   // Function to initialize and start multiple odometers
   document.addEventListener("DOMContentLoaded", function() {

      const image = document.getElementById("zoomableImage");

      image.addEventListener("click", function() {
         this.classList.toggle("zoom-in"); // Toggle zoom-in class
      });

      // Select all odometer elements
      const odometers = document.querySelectorAll(".odometer");
      console.log(odometers);
      // Iterate through each odometer and set its target value
      odometers.forEach((odometer) => {
         const targetValue = odometer.getAttribute("data-target"); // Get target value from data attribute
         setTimeout(() => {
            odometer.innerHTML = targetValue; // Update the value
         }, 100); // Optional delay before starting animation
      });
   });
</script>